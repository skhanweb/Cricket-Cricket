                    <p>The origin of cricket is unknown. Most probably, its name was derived from the Old English cryce, which means "stick," and, in its rude form, resembled the 13th century game known as club-ball.</p>
					<p>Cricket evolved in England in the 18th century, mainly because of the interest of great landowners who tried their skills on a field of play with their tenants and the local peasantry. Records show that teams from Kent and London played each other in 1719, and that Kent and Sussex met in 1728.</p>
					<p>The earliest written laws (rules) date back to 1744. The Hambledon Club in Hampshire was the focal point of cricket from 1768 to about 1788. It attracted the chief patrons and best cricketers in the land and was the place where cricket took a great step forward from the rather rustic pastime that it was to the game it is today.</p>
					<p>In 1787, Thomas Lord, a Yorkshireman, opened a cricket ground in London, and in that year the Marylebone Cricket Club was formed.</p>
					<p>Today the present Lord's at St. John's Wood is the most famous cricket venue in the world and the M. C. C. is the authoritative source of all cricket legislation.</p>
					<p>As early as 1859 an All-England team toured Canada and the United States, and in 1861 a team toured Australia. Australia won the first recorded international match in Melbourne in 1877, defeating England by 45 runs. 5 years later, in 1882 Australia won again in London.</p>
					<p>The Sporting Times in a mock obituary said "In affectionate remembrance of English cricket. â€¦ The body will be cremated, and the Ashes taken to Australia."</p>
					<p>Since then matches between England and Australia, called The Ashes, have been the highlight of cricket competition. Other participants in Test matches include South Africa, the West Indies, New Zealand, India, and Pakistan. The ruling body for the Test matches is the International Cricket Conference, founded in 1909 as the Imperial Cricket Conference.</p>
					<p>The year of the first official championships between the counties in England is recognized in 1890. In 1904 the M. C. C. formed the Advisory County Cricket Committee, which has dealt with every aspect of this major English contest since.</p>
					<p>The Women's Cricket Association was founded in England in 1926. Women compete on an amateur basis. In 1958 the International Women's Cricket Council was formed.</p>
					
