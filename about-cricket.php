                        <p><span class="callout">Cricket</span> is a ball and sport played by 2 teams of 11 players. A match consists of 2 halves (so that each team bats and bowls) and is played on a grass field of around 15,000 mÂ² in area. 11 "fielding/bowling team" players play against 2 "batting team" (hitting) players at any one time.</p>
