						<p>The equipment includes a bat made of willow with a flat surface, a ball (usually red), and two wickets (made up of three vertical stumps and two small bails resting on top of the stumps). Bowlers try to hit the wicket with the ball when bowling and to break it. At the same time the batsman uses his bat to stop the ball hitting the wickets and if he hits the ball can score runs (points). While the fielding team is retrieving the ball, the batsman run backwards and forwards to score runs. If the bat						sman does not manage to reach the wicket he is running to and a fielder breaks the wicket with the ball, he is out (in this instance: run out) and the next batsman replaces him. There are ten different ways of getting out - the most common of which are being "caught" - a fielder catches the ball after it has been hit without the ball having touched the ground in the mean-time, or "bowled" when the bowler breaks the wicket with the ball.</p>
						
						
					<p>The team bats until either a pre-determined number of balls have been bowled, or until all batsman are out (as there have to be two players batting, the team is all out when ten batsmen have got out (bowled, caught, run out etc.). Then the team that bowled first bats and tries to score more runs than the other team. The team that was batting becomes the bowling team. The team with the higher total of runs wins.</p>
                   
