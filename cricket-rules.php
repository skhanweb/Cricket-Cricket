                    <h2>The Simple Rules of Cricket</h2>
                    
                                    
                    <div class="articleBody clear">
                    
                    	<figure> <!-- The figure tag marks data (usually an image) that is part of the article -->
	                    <img src="img/cricketrules.jpg" width="400" height="230" alt="Img_of_Cricket_Rules"/>
