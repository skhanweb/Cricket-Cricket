                    <p>Cricket is played between two teams of up to 11 players each. Each team bats (takes its innings) in turn, the choice for first innings being decided by tossing a coin.</p>
					<p>The centre of the action is a pitch 22 yards long with wickets (three cricket stumps) placed at each end, though a shorter pitch can be used by children. The pitch is roughly in the centre of the playing area, the edge of which is marked by a line or a rope about 50 yards from the wickets.</p>
					
					<p>Two batsmen play in partnership to score runs against the bowling of the fielding side. The bowlers aim to restrict scoring and to dismiss the batsmen in one of the ways described in the rules of cricket.</p>
					
					<p>The fielding captain decides which of his players will bowl. Each bowler bowls an “over” of six overarm deliveries from one end of the pitch, then the play switches so that one over is bowled alternately from each end of the pitch. No bowler can deliver two overs in succession.</p>
					
						
					
					<p>The score is counted by “runs”, which are the number of times the batsmen run from end to end of the area between the two “popping creases” – lines across the pitch four feet from each wicket. Runs are usually the result of a hit by the bat; a hit across the boundary scores four runs, or six if it crosses without touching the ground.</p>
					
					
					<p>The most common ways of dismissing a batsman are bowled, caught off the bat, leg before wicket, stumped by the wicketkeeper and the run out of either batsman while attempting a run. When a batsman is dismissed (“out”) his place is taken by another.</p>
					<figure> <!-- The figure tag marks data (usually an image) that is part of the article -->
	                    <img src="img/cricketrules2.jpg" width="400" height="230" alt="Img_of_Cricket_Rules"/>
						</figure>
					<p>The innings is complete either when all but one of the batsmen are out, or when an agreed number of overs has been bowled, or when the batting captain has declared the innings closed.</p>
					<p>A match consists of one or two innings by each side. In time limited matches, the side scoring the highest aggregate of runs wins. If the match is not played to a finish then the result is a draw, except in the case of some limited overs games where the winning side is the one that scored most runs during its share of the overs.</p>
					<p>Want to know more? The rules of cricket are described in detail on the Marylebone Cricket Club Website. And once you understand the basics, its easy to build up your knowledge so that the laws of the game become second nature. </p>
					
					
                   
